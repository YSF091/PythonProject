A = [76, 35, 89, 12, 45, 26, 90]
a = len(A)                                 # 得知A裡面有幾個元素

for i in range(a - 1):                     # 會執行a - 1次，因為最後一個數字會在前面數字排好的時候排完
    for j in range(a - 1 - i):             # 要比較的範圍是0 ~ a - 2，所以用range的話會是a - 2 + 1，隨著i增加，i的元素已經被排好了。可以不用比較，因而減去
        if A[j] < A[j +1]:
            A[j], A[j +1] = A[j + 1], A[j]

    print(f"{i}:", A)
